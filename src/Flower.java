public class Flower {
    double sepalLength;
    double sepalWidth;
    double petalLength;
    double petalWidth;

    public Flower(double sepalLength, double sepalWidth, double petalLength, double petalWidth) {
        this.sepalLength = sepalLength;
        this.sepalWidth = sepalWidth;
        this.petalLength = petalLength;
        this.petalWidth = petalWidth;
    }
}
